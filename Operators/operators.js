//Logical Operators

let a1 = 9
let b2 = 17
let cond1 = a1 == a1
let cond2 = a1 >= a1

//Logical And &&
console.log(a1 == a1 && a1 >= a1);
console.log(cond1 && cond2);
console.log(1 + 2 - 7 == -4 && -4 >= 1 + 2 - 7);
console.log(b2 == b2 && "b2" == 'b2');
console.log(59 != 56 && 78 >= 20);
