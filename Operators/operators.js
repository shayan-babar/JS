//Arithmetic Operators

let x = 39
let y = 27

// +,-,*,/
console.log(x - y);
console.log(y * x);
console.log(x / 99);
console.log(y + "70");
console.log(x - y * y / x);

//Modulus
console.log(x % y);
console.log(y % x);
console.log(x % 6.9);
console.log(7.8 % 3.6);

//Exponentiation
console.log(x ** 2);
console.log(3 ** 3);
console.log(x ** y);
console.log(9 ** 2);
console.log(5 ** 5);

//Increment
let s = 39
let g = 27
console.log(s++);
console.log(g++);
